# definição de conjuntos
E = float(input("Primeiro conjunto: "))
N = float(input("segundo conjunto: "))

# set uniao
print("União de E e N é",E | N)

# set interseção
print("Interseção de E e N é",E & N)

# set diferença
print("Differença de E e N é",E - N)

# set differença simetrica
print("Differença simetrica de E e N é",E ^ N)
